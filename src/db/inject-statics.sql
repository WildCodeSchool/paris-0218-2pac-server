  <p>&emsp;&emsp;&emsp;En parallèle, la résilience des fermes s’exprime aussi face aux <b> aléas climatiques et sanitaires</b>. Certes, l’agriculture demeure par essence une activité soumise aux conditions météorologiques, surtout que les événements extrêmes sont devenus de plus en plus fréquents, et aux épidémies frappant tant les cultures que les animaux d’élevage. Mais les paysans ont <b> adapté leurs pratiques</b>, de telle sorte qu’ils minimisent les risques encourus en cas de survenance d’un aléa climatique ou sanitaire. Par exemple, ils choisissent des <b> races d’animaux </b> rustiques adaptées aux territoires ou des <b> espèces de végétaux naturellement résistantes </b>
