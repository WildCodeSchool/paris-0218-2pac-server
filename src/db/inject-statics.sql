  <p>Les paiements de la PAAC constituent un levier financier fondamental pour guider les filières agro-alimentaires vers une transition agroécologique. Pour ce faire, leurs critères d’allocation doivent se baser sur deux outils : une conditionnalité liée à des normes réglementaires et la rémunération des services rendus à la société. D’une part, la PAAC se fonde sur une triple conditionnalité environnementale (protection de l’eau, de l’air, de la biodiversité, des sols et du bien-être animal), sociale (droit du travail pour les salarié·e·s agricoles) et économique (modulation et plafonnement des aides par actif). D’autre part, elle passe d’une logique de palliation des effets négatifs de l’agriculture productiviste à celle de valorisation des externalités positives, via une rétribution proportionnelle aux engagements des agriculteur·rice·s en faveur de pratiques vertueuses. Ainsi, la transition agroécologique doit être soutenue sans distorsion entre les territoires ni exemptions entre les paysan·ne·s, en tenant compte des efforts fournis et non de références agricoles caduques.</p>
