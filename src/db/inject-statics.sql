  <p>La PAAC doit être une politique agricole nourricière, sans incitation à une augmentation systématique des volumes de production, mais en reliant ces derniers à la demande européenne concernant d’une part, des denrées de qualité sur le plan sanitaire, en particulier exemptes de pesticides, et d’autre part, des régimes alimentaires équilibrés, notamment moins riches en protéines animales. La recherche d’une souveraineté alimentaire européenne suppose de réduire la dépendance aux productions extra-communautaires pour en favoriser unerelocalisée et diversifiée, respectueuse de la spécificité des territoires de l’UE, au bénéfice conjoint des paysan·ne·s et des consommateur·rices·s européen·ne·s.</p>
