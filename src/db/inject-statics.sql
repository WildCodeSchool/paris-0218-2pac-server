  <h2>Mettre la future Politique Agricole et Alimentaire Commune au service de tous les citoyen·ne·s</h2>
  <p>Une politique agricole et alimentaire constitue bien plus qu’un budget sectoriel : c’est un véritable enjeu de société. Il est urgent de créer un pacte entre paysan·ne·s et citoyen·ne·s européen·ne·s, qui impulse à grande échelle la nécessaire mutation du système agro-industriel et qui scelle leur détermination conjointe à développer un modèle alimentaire durable. De là dépend la pérennité des moyens de production de l’agriculture de l’UE, au premier rang desquels figurent les paysan·ne·s et par conséquent, leur rémunération par des prix justes et stables au sein de filières équitables. Une PAAC pour tous s’adresse donc aussi aux agriculteur·rice·s dont le modèle de production ne correspond plus ni à la réalité d’un marché mondial libéralisé, ni aux attentes des citoyen·ne·s, par le biais de contrats de transition aux conditions et échéances clairement définis.</p>
  <br/>
